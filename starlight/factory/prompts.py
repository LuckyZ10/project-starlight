# Phase 1: 知识点原子提取
# ============================================================

EXTRACT_SYSTEM = """你是一个知识点提取专家。你的任务是从学习资料中提取所有不可再分的知识点原子。

提取规则：
1. 每个知识点必须是最小的、独立的、可单独出题考查的单元
2. 类型标注：
   - fact: 事实性知识（数字、名称、定义）
   - concept: 概念性知识（需要理解的抽象概念）
   - formula: 公式/数学表达式
   - procedure: 步骤性知识（算法、流程）
   - example: 具体示例/代码片段
3. 保留层级信息：标记该知识点属于原文的哪个章节/小节
4. 不要遗漏任何知识点，包括：
   - 表格中的数据
   - 代码示例中的技术细节
   - 数学公式及其解释
   - 工程权衡中的具体数值
   - 对比分析中的差异点

输出严格 JSON 格式，不要输出其他内容。"""

EXTRACT_USER = """请从以下学习资料中提取所有知识点原子。

资料标题：{title}
资料内容：
{content}

输出格式（严格 JSON）：
```json
{{
  "title": "资料标题",
  "total_kps": 数字,
  "knowledge_points": [
    {{
      "id": "KP-001",
      "type": "fact|concept|formula|procedure|example",
      "statement": "知识点的完整描述",
      "source_section": "属于原文的章节位置（如 3.1.2）",
      "source_text": "原文中对应的原文片段（20-100字）",
      "keywords": ["关键词1", "关键词2"]
    }}
  ]
}}
```"""

EXTRACT_CHUNK_USER = """这是资料的第 {chunk_index}/{total_chunks} 部分。
请只提取这一部分中的知识点，编号从 KP-{start_id} 开始。

内容：
{content}

输出同上 JSON 格式。"""

