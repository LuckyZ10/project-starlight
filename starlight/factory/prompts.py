# Phase 2: 分组 + DAG 构建
# ============================================================

BUILD_SYSTEM = """你是一个课程设计专家。你需要将知识点原子分组为学习节点，并构建前置依赖关系图（DAG）。

分组原则：
1. 每个节点应包含 3-8 个强相关的知识点（构成一个完整的学习单元）
2. 同一节的知识点应属于同一主题领域
3. 前置关系必须严格反映学习依赖（不懂 A 就不可能学 B）
4. 难度标定：1=入门，2=基础，3=中级，4=高级，5=专家
5. pass_criteria 必须具体、可验证（不要写"理解XX"，要写"能解释XX并举例说明"）

DAG 构建规则：
1. 必须有且仅有一个入口节点（无前置）
2. 不能有环（A→B→C→A 禁止）
3. 从入口到每个节点都必须可达
4. 前置关系应尽可能少，只保留真正的学习依赖

输出严格 JSON 格式。"""

BUILD_USER = """请将以下 {total_kps} 个知识点分组为学习节点，并构建 DAG。

知识点列表：
{kps_json}

输出格式（严格 JSON）：
```json
{{
  "nodes": [
    {{
      "id": "N01",
      "title": "节点标题（简洁中文）",
      "kp_ids": ["KP-001", "KP-002"],
      "prerequisites": [],
      "difficulty": 1-5,
      "pass_criteria": "具体的通过标准",
      "summary": "本节一句话概述"
    }}
  ],
  "dag": {{
    "entry": "N01",
    "edges": {{
      "N01": ["N02", "N03"],
      "N02": ["N04"]
    }}
  }}
}}
```"""

